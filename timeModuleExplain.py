import time 
timestamp = time.strftime('%H:%M:%S')
print(timestamp)
timestamp = time.strftime('%H')
print(timestamp)
timestamp = time.strftime('%M')
print(timestamp)
timestamp = time.strftime('%S')
print(timestamp)

'''
The strftime function is used to format the time into a readable
string according to the specified format codes. It provides a
convenient way to extract and format various components of the
current time.
'''